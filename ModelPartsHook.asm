PUBLIC hkModelParts

EXTERN g_pCamera: QWORD

_TEXT SEGMENT

hkModelParts PROC

PUSH RDX
MOV R15D, R12D
CMP RAX, 70h
MOV RDX, QWORD PTR [g_pCamera]
ADD RDX, 0A8h
MOV RDX, QWORD PTR [RDX]
ADD RDX, 3A0h
CMP RDX, RCX
JNE UPDATE
JMP RETURN

UPDATE:
MOVAPS XMMWORD PTR [RCX + RAX + 10h], XMM0
MOV	   DWORD PTR [RCX + RAX + 68h], EDI

RETURN:
POP RDX
RET

hkModelParts ENDP

_TEXT ENDS
END